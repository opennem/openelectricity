<script>
	import { getContext } from 'svelte';
	import StackedAreaChart from '$lib/components/charts/StackedAreaChart.svelte';
	import LineChart from '$lib/components/charts/LineChart.svelte';

	import Tooltip from './Tooltip.svelte';

	export let store;
	/** @type {string[]} */
	export let hiddenRowNames = [];

	const {
		title,
		allowPrefixSwitch,
		displayPrefix,
		displayUnit,
		convertAndFormatValue,
		seriesData,
		seriesNames,
		seriesColours,
		seriesLabels,
		yDomain,
		xTicks,
		formatTickX,
		chartType,
		isChartTypeArea,
		chartOverlay,
		chartOverlayLine,
		chartOverlayHatchStroke,
		chartHeightClasses,
		hoverKey,
		hoverData,
		focusData,
		getNextPrefix
	} = store;

	const {
		singleSelectionModelScenarioLabel,
		singleSelectionPathway,
		selectedRegionLabel,
		isScenarioViewSection
	} = getContext('scenario-filters');

	$: names = $seriesNames.filter((/** @type {string} */ d) => !hiddenRowNames.includes(d));
	$: colours = names.map((/** @type {string} */ d) => $seriesColours[d]);

	function moveToNextDisplayPrefix() {
		$displayPrefix = getNextPrefix();
	}
</script>

<section>
	{#if names.length}
		<header>
			<div class="md:flex gap-2 items-center px-10 md:px-0">
				<div class="flex gap-2 items-center">
					<h5 class="m-0 leading-none">
						{$title}
					</h5>

					{#if $allowPrefixSwitch}
						<button
							class="font-light text-sm text-mid-grey hover:underline"
							on:click={moveToNextDisplayPrefix}
						>
							{$displayUnit || ''}
						</button>
					{:else}
						<span class="font-light text-sm text-mid-grey">{$displayUnit || ''}</span>
					{/if}

					<span class="hidden md:block font-light text-sm text-mid-grey">â€”</span>
				</div>

				<span class="font-light text-xs md:text-sm text-mid-grey relative -top-1 md:top-0">
					{$singleSelectionModelScenarioLabel} ({$singleSelectionPathway}), {$selectedRegionLabel}
				</span>
			</div>

			<Tooltip
				hoverData={$hoverData}
				hoverKey={$hoverKey}
				seriesColours={$seriesColours}
				seriesLabels={$seriesLabels}
				convertAndFormatValue={$convertAndFormatValue}
				showTotal={$isChartTypeArea ? true : false}
			/>
		</header>

		{#if $isChartTypeArea || $isScenarioViewSection}
			<StackedAreaChart
				dataset={$seriesData}
				xKey="date"
				yKey={[0, 1]}
				zKey="key"
				xTicks={$xTicks}
				yTicks={2}
				yDomain={$yDomain}
				seriesNames={names}
				zRange={colours}
				formatTickX={$formatTickX}
				formatTickY={$convertAndFormatValue}
				chartType={$chartType}
				overlay={$chartOverlay}
				overlayLine={$chartOverlayLine}
				overlayStroke={$chartOverlayHatchStroke}
				hoverData={$hoverData}
				focusData={$focusData}
				chartHeightClasses={$chartHeightClasses}
				on:mousemove
				on:mouseout
				on:pointerup
			/>
		{:else}
			<LineChart
				dataset={$seriesData}
				xKey="date"
				yKey={$seriesNames[0]}
				zKey={colours[0]}
				xTicks={$xTicks}
				yTicks={2}
				formatTickX={$formatTickX}
				formatTickY={$convertAndFormatValue}
				overlay={$chartOverlay}
				overlayLine={$chartOverlayLine}
				overlayStroke={$chartOverlayHatchStroke}
				hoverData={$hoverData}
				focusData={$focusData}
				showArea={false}
				chartHeightClasses={$chartHeightClasses}
				on:mousemove
				on:mouseout
				on:pointerup
			/>
		{/if}
	{/if}
</section>
